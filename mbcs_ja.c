int mbcs_lead_byte(int code)
{
	if (code >= 0x100)
		return 0;

	switch (Option.jcode) {
	case JCODE_ASCII:
		return mbcs_map_ascii[code & 0xff];
	case JCODE_SJIS:
		return mbcs_map_sjis[code & 0xff];
	case JCODE_EUC:
		return mbcs_map_euc[code & 0xff];
	case JCODE_UTF8:
		return mbcs_map_utf8[code & 0xff];
	default:
		return 0;
	}
}

